$('#plugins-sidebar').on('click', 'li', function(){
	if (!$(this).hasClass('selected')) {
		var url = $(this).data('url');
		$('iframe').prop('src', url);
		$('#plugins-sidebar').find('li.selected').removeClass('selected');
		$(this).addClass('selected');
	}
}).on('click', 'h2', function(){
	$('#plugins-sidebar').find('h2.selected').removeClass('selected').end().find('ul').hide();
	var isVisible = $(this).next().is(':visible');
	if (isVisible) {
		$(this).next().hide();
	} else {
		$(this).next().show();
		$(this).addClass('selected');
	}
});

$('iframe').prop('src', "default-content.html");